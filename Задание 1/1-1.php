Bloomberg также отмечает, что неизвестно, смогут ли лидеры стран блока сформировать единую позицию. Так, например, в ходе встречи послов Евросоюза, которая прошла в среду, 10 марта, Италия и Германия предложили определить области избирательного сотрудничества с Россией, а Польша, Эстония и Литва выступили за более жесткий подход.</p>
