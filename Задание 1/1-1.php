	<p>Некоторые из самых высокопоставленных чиновников Европейского союза (ЕС) в частном порядке выступили за «менее воинственный подход» к отношениям с Россией. Об этом сообщает Bloomberg со ссылкой на источник, ознакомленный с обсуждениями в ходе ужина у главы европейской дипломатии Жозепа Борреля.

